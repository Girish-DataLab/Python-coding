# Operator
# An operator is a symbol used to perform operations on variables and values.

# 1. Arithmetic Operators
# Arithmetic operators are used to perform mathematical calculations.

a = 10
b = 3

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a // b)
print(a ** b)

